import { readFileSync } from 'node:fs'
import { join } from 'node:path'
import { describe, expect, it } from 'vitest'
import type { OpenAPIV3_1 } from 'openapi-types'
import { generateTestUtils } from '../plugins/test-utils.js'

// Load fixture directly (no parseSpec — avoids needing openapi-gen dist built)
const fixturePath = join(import.meta.dirname, '../../__fixtures__/specs/task-hooks.json')
const spec = JSON.parse(readFileSync(fixturePath, 'utf-8')) as OpenAPIV3_1.Document

const { filename, content } = generateTestUtils(spec)

describe('generateTestUtils', () => {
  it('filename is test-utils.ts', () => {
    expect(filename).toBe('test-utils.ts')
  })

  it('contains auto-generated header comment', () => {
    expect(content).toContain(
      '// This file is auto-generated by @codewithagents/openapi-react-query — do not edit',
    )
  })

  it('imports QueryClient from @tanstack/react-query (existing peer dep — no new deps)', () => {
    expect(content).toContain("from '@tanstack/react-query'")
    expect(content).toContain('QueryClient')
  })

  it('does NOT import from any testing framework (vitest, jest, testing-library)', () => {
    expect(content).not.toContain("from 'vitest'")
    expect(content).not.toContain("from '@vitest/")
    expect(content).not.toContain("from 'jest'")
    expect(content).not.toContain("from '@testing-library/")
    expect(content).not.toContain("from 'msw'")
  })

  it('exports createTestQueryClient function', () => {
    expect(content).toContain('export function createTestQueryClient(')
  })

  it('createTestQueryClient returns a QueryClient with retry disabled', () => {
    expect(content).toContain('retry: false')
  })

  it('createTestQueryClient disables gcTime or sets it to a deterministic value', () => {
    // Ensures queries don't linger between tests with stale data
    expect(content).toContain('gcTime:')
  })

  it('exports createWrapper function', () => {
    expect(content).toContain('export function createWrapper(')
  })

  it('createWrapper uses QueryClientProvider (from the same peer dep)', () => {
    expect(content).toContain('QueryClientProvider')
  })

  it('createWrapper returns a component usable as wrapper in renderHook', () => {
    // The wrapper component must accept { children } — ReactNode from react (also peer dep)
    expect(content).toContain('children')
  })

  it('does not import from msw or any test-runner-specific module', () => {
    // The file should be completely test-runner agnostic
    expect(content).not.toContain('msw')
    expect(content).not.toContain('vi.fn')
    expect(content).not.toContain('jest.fn')
  })
})
