import { describe, it, expect } from 'vitest'
import { generateServer } from '../plugins/server.js'
import type { OpenAPIV3_1 } from 'openapi-types'

function makeSpec(paths: OpenAPIV3_1.PathsObject): OpenAPIV3_1.Document {
  return {
    openapi: '3.1.0',
    info: { title: 'Test', version: '1.0.0' },
    paths,
  }
}

const taskSpec = makeSpec({
  '/tasks': {
    get: { operationId: 'listTasks', responses: { '200': { description: 'ok' } } },
    post: { operationId: 'createTask', responses: { '201': { description: 'created' } } },
  },
  '/tasks/{id}': {
    get: { operationId: 'getTask', responses: { '200': { description: 'ok' } } },
    put: { operationId: 'updateTask', responses: { '200': { description: 'ok' } } },
    delete: { operationId: 'deleteTask', responses: { '204': { description: 'no content' } } },
  },
})

describe('generateServer — output file', () => {
  it('returns filename server.ts', () => {
    expect(generateServer(taskSpec).filename).toBe('server.ts')
  })

  it('starts with the auto-generated header comment', () => {
    const { content } = generateServer(taskSpec)
    expect(
      content.startsWith('// This file is auto-generated by @codewithagents/openapi-gen')
    ).toBe(true)
  })

  it('imports ClientConfig from ./client-config.js', () => {
    const { content } = generateServer(taskSpec)
    expect(content).toContain("import type { ClientConfig } from './client-config.js'")
  })

  it('imports all client functions from ./client.js', () => {
    const { content } = generateServer(taskSpec)
    expect(content).toContain("from './client.js'")
    expect(content).toContain('listTasks')
    expect(content).toContain('createTask')
    expect(content).toContain('getTask')
    expect(content).toContain('updateTask')
    expect(content).toContain('deleteTask')
  })

  it('does NOT define the DropLast helper type', () => {
    const { content } = generateServer(taskSpec)
    expect(content).not.toContain('DropLast')
  })

  it('exports createServerClient function', () => {
    const { content } = generateServer(taskSpec)
    expect(content).toContain('export function createServerClient(config: Partial<ClientConfig>)')
  })

  it('functions with no params (no path/body/query) use () => fn(config) pattern', () => {
    // In taskSpec, listTasks and createTask have no path params, no body, no query params
    const { content } = generateServer(taskSpec)
    expect(content).toContain('listTasks: () => listTasks(config),')
    expect(content).toContain('createTask: () => createTask(config),')
  })

  it('functions with path param only use explicit Parameters<typeof fn>[N] signature', () => {
    const { content } = generateServer(taskSpec)
    // getTask, updateTask, deleteTask all have path param 'id'
    expect(content).toContain(
      'getTask: (id: Parameters<typeof getTask>[0]) => getTask(id, config),'
    )
    expect(content).toContain(
      'deleteTask: (id: Parameters<typeof deleteTask>[0]) => deleteTask(id, config),'
    )
  })

  it('config is NOT included as a typed parameter in any factory method', () => {
    const { content } = generateServer(taskSpec)
    // No function should expose config as a typed param (it's always the trailing arg in the call, not the sig)
    expect(content).not.toContain('Parameters<typeof listTasks>[1]')
    expect(content).not.toContain('Parameters<typeof createTask>[1]')
    // Single-path-param functions: config is index 1, should not appear as typed sig param
    expect(content).not.toContain('Parameters<typeof getTask>[1]')
    expect(content).not.toContain('Parameters<typeof deleteTask>[1]')
  })
})

describe('generateServer — explicit params with body and query', () => {
  const specWithBody = makeSpec({
    '/tasks': {
      get: {
        operationId: 'listTasks',
        parameters: [{ name: 'status', in: 'query', schema: { type: 'string' } }],
        responses: { '200': { description: 'ok' } },
      },
      post: {
        operationId: 'createTask',
        requestBody: {
          required: true,
          content: { 'application/json': { schema: { $ref: '#/components/schemas/Task' } } },
        },
        responses: { '201': { description: 'created' } },
      },
    },
    '/tasks/{id}': {
      put: {
        operationId: 'updateTask',
        requestBody: {
          required: true,
          content: { 'application/json': { schema: { $ref: '#/components/schemas/Task' } } },
        },
        responses: { '200': { description: 'ok' } },
      },
    },
  })

  it('body-only mutation uses (body: Parameters<typeof fn>[0]) signature', () => {
    const { content } = generateServer(specWithBody)
    expect(content).toContain(
      'createTask: (body: Parameters<typeof createTask>[0]) => createTask(body, config),'
    )
  })

  it('path param + body uses explicit positional signatures', () => {
    const { content } = generateServer(specWithBody)
    expect(content).toContain(
      'updateTask: (id: Parameters<typeof updateTask>[0], body: Parameters<typeof updateTask>[1]) => updateTask(id, body, config),'
    )
  })

  it('optional query params use params? signature', () => {
    const { content } = generateServer(specWithBody)
    expect(content).toContain(
      'listTasks: (params?: Parameters<typeof listTasks>[0]) => listTasks(params, config),'
    )
  })
})

describe('generateServer — required query params', () => {
  const specWithRequiredQuery = makeSpec({
    '/search': {
      get: {
        operationId: 'searchItems',
        parameters: [{ name: 'q', in: 'query', required: true, schema: { type: 'string' } }],
        responses: { '200': { description: 'ok' } },
      },
    },
  })

  it('required query params use params (non-optional) signature', () => {
    const { content } = generateServer(specWithRequiredQuery)
    expect(content).toContain(
      'searchItems: (params: Parameters<typeof searchItems>[0]) => searchItems(params, config),'
    )
    expect(content).not.toContain('params?:')
  })
})

describe('generateServer — empty spec', () => {
  it('handles spec with no paths gracefully', () => {
    const emptySpec = makeSpec({})
    const { content } = generateServer(emptySpec)
    expect(content).toContain('export function createServerClient')
    // no client import when there are no functions
    expect(content).not.toContain("from './client.js'")
  })

  it('handles spec with no paths property', () => {
    const spec = { openapi: '3.1.0', info: { title: 'T', version: '1' } } as OpenAPIV3_1.Document
    expect(() => generateServer(spec)).not.toThrow()
  })
})

describe('generateServer — derived operation names (no operationId)', () => {
  it('derives function names from method + path when operationId is absent', () => {
    const spec = makeSpec({
      '/api/v1/items': {
        get: { responses: { '200': { description: 'ok' } } },
        post: { responses: { '201': { description: 'created' } } },
      },
      '/api/v1/items/{id}': {
        get: { responses: { '200': { description: 'ok' } } },
        delete: { responses: { '204': { description: 'no content' } } },
      },
    })
    const { content } = generateServer(spec)
    // No path params, no body, no query params → () => fn(config)
    expect(content).toContain('getItems: () => getItems(config),')
    expect(content).toContain('createItems: () => createItems(config),')
    // With path param 'id' → explicit param signature
    expect(content).toContain(
      'getItemsById: (id: Parameters<typeof getItemsById>[0]) => getItemsById(id, config),'
    )
    expect(content).toContain(
      'deleteItemsById: (id: Parameters<typeof deleteItemsById>[0]) => deleteItemsById(id, config),'
    )
  })
})

describe('coverage: $ref parameters are skipped in operationHasQueryParams / operationHasRequiredQueryParams', () => {
  it('operation with a $ref parameter and an inline query param generates correctly', () => {
    // $ref parameters are skipped (return false early). The inline query param is still picked up.
    const spec = makeSpec({
      '/search': {
        get: {
          operationId: 'search',
          parameters: [
            // eslint-disable-next-line @typescript-eslint/no-explicit-any
            { $ref: '#/components/parameters/PageParam' } as any,
            { name: 'q', in: 'query', required: false, schema: { type: 'string' } },
          ],
          responses: { '200': { description: 'ok' } },
        },
      },
    })
    const { content } = generateServer(spec)
    // Route is generated and picks up the inline query param 'q'
    expect(content).toContain('search: (params?: Parameters<typeof search>[0])')
  })

  it('operation with only a $ref parameter is treated as having no query params', () => {
    const spec = makeSpec({
      '/search': {
        get: {
          operationId: 'searchOnly',
          parameters: [
            // eslint-disable-next-line @typescript-eslint/no-explicit-any
            { $ref: '#/components/parameters/PageParam' } as any,
          ],
          responses: { '200': { description: 'ok' } },
        },
      },
    })
    const { content } = generateServer(spec)
    // No inline query params found, so the method has no params signature
    expect(content).toContain('searchOnly: () => searchOnly(config)')
  })
})
