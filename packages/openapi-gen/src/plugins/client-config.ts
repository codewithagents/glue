import type { GeneratedFile } from './types.js'
import type { AuthSchemes } from './client.js'

export interface GenerateClientConfigOptions {
  defaultCredentials?: string
  authSchemes?: AuthSchemes
}

// Static segments shared by every generated ClientConfig file.
const HEADER_LINES = [
  `// This file is auto-generated by @codewithagents/openapi-gen — do not edit`,
  ``,
  `export interface ClientConfig {`,
  `  /** Base URL for all API requests (e.g. 'https://api.example.com') */`,
  `  baseUrl: string`,
  `  /**`,
  `   * Bearer token or a function that returns one (supports async refresh).`,
  `   * When provided, adds \`Authorization: Bearer <token>\` to every request.`,
  `   */`,
  `  token?: string | (() => string | Promise<string>)`,
]

const BASIC_AUTH_FIELD_LINES = [
  `  /**`,
  `   * HTTP Basic credentials or a function that returns them.`,
  `   * When provided, adds \`Authorization: Basic <base64(username:password)>\` to every request.`,
  `   */`,
  `  basicAuth?: { username: string; password: string } | (() => { username: string; password: string } | Promise<{ username: string; password: string }>)`,
]

const COMMON_FIELDS_LINES = [
  `  /**`,
  `   * Fetch credentials mode. Use 'include' for cookie-based auth.`,
  `   * Defaults to 'same-origin'.`,
  `   */`,
  `  credentials?: RequestCredentials`,
  `  /** Additional headers sent with every request */`,
  `  headers?: Record<string, string>`,
  `  /**`,
  `   * Global error hook called with every non-2xx response error before it is`,
  `   * thrown. Use for logging, monitoring, or triggering auth refresh flows.`,
  `   *`,
  `   * @example`,
  `   * configureClient({`,
  `   *   onError: (err) => Sentry.captureException(err),`,
  `   * })`,
  `   */`,
  `  onError?: (err: Error) => void`,
  `}`,
  ``,
]

const RUNTIME_LINES = [
  `/** Configure the API client. Call once at app startup before making any requests. */`,
  `export function configureClient(config: ClientConfig): void {`,
  `  _config = { ..._config, ...config }`,
  `}`,
  ``,
  `/** @internal — used by generated fetch functions */`,
  `export function getConfig(): Readonly<ClientConfig> {`,
  `  return _config`,
  `}`,
  ``,
]

const INIT_SAME_ORIGIN = [
  `let _config: ClientConfig = {`,
  `  baseUrl: '',`,
  `  credentials: 'same-origin',`,
  `}`,
  ``,
]

const INIT_INCLUDE = [
  `// Cookie auth: send session cookie automatically`,
  `let _config: ClientConfig = {`,
  `  baseUrl: '',`,
  `  credentials: 'include',`,
  `}`,
  ``,
]

/** Returns true when the spec declares http basic auth. */
function specHasBasicAuth(auth: AuthSchemes | undefined): boolean {
  return auth ? auth.hasBasicAuth : false
}

/** Returns true when the spec declares an apiKey scheme (header or query). */
function specHasApiKey(auth: AuthSchemes | undefined): boolean {
  return auth ? auth.apiKeyHeaderNames.length > 0 || auth.apiKeyQueryNames.length > 0 : false
}

/** Returns the apiKey field lines with a comment describing where the key is placed. */
function apiKeyFieldLines(auth: AuthSchemes): string[] {
  return [
    `  /**`,
    `   * API key or a function that returns one.`,
    `   * ${buildApiKeyPlacementComment(auth)}`,
    `   */`,
    `  apiKey?: string | (() => string | Promise<string>)`,
  ]
}

function buildApiKeyPlacementComment(auth: AuthSchemes): string {
  const parts: string[] = []
  if (auth.apiKeyHeaderNames.length > 0) {
    parts.push(`Sent as header ${auth.apiKeyHeaderNames.map((n) => `'${n}'`).join(', ')}`)
  }
  if (auth.apiKeyQueryNames.length > 0) {
    parts.push(`appended as query param ${auth.apiKeyQueryNames.map((n) => `'${n}'`).join(', ')}`)
  }
  return parts.join('; ') + ' on every request.'
}

/**
 * Builds a ClientConfig interface string.
 *
 * `token?` is always present for back-compat — bearer specs and consumers passing `token`
 * continue to work without changes.
 *
 * `basicAuth?` is emitted ONLY when the spec declares an http basic scheme, and
 * `apiKey?` is emitted ONLY when the spec declares an apiKey (header or query) scheme.
 * This prevents dead fields on no-auth and bearer-only specs.
 */
function buildClientConfigContent(options?: GenerateClientConfigOptions): string {
  const auth = options?.authSchemes
  const segments: string[][] = [HEADER_LINES]
  if (specHasBasicAuth(auth)) segments.push(BASIC_AUTH_FIELD_LINES)
  if (specHasApiKey(auth)) segments.push(apiKeyFieldLines(auth!))
  segments.push(COMMON_FIELDS_LINES)
  segments.push(options?.defaultCredentials === 'include' ? INIT_INCLUDE : INIT_SAME_ORIGIN)
  segments.push(RUNTIME_LINES)
  return segments.flat().join('\n')
}

export function generateClientConfig(options?: GenerateClientConfigOptions): GeneratedFile {
  return {
    filename: 'client-config.ts',
    content: buildClientConfigContent(options),
  }
}
