import type { OpenAPIV3_1 } from 'openapi-types'
import type { GeneratedFile } from './types.js'

type OperationObject = OpenAPIV3_1.OperationObject
type ReferenceObject = OpenAPIV3_1.ReferenceObject

const SUPPORTED_METHODS = ['get', 'post', 'put', 'patch', 'delete'] as const

function deriveOperationName(method: string, path: string): string {
  const prefixMap: Record<string, string> = {
    get: 'get',
    post: 'create',
    put: 'update',
    patch: 'patch',
    delete: 'delete',
  }
  const prefix = prefixMap[method] ?? method

  const segments = path.replace(/^\/api\/v\d+\//, '').replace(/^\//, '')
  const parts = segments.split('/').map((seg) => {
    if (seg.startsWith('{') && seg.endsWith('}')) {
      const name = seg.slice(1, -1)
      return 'By' + name.charAt(0).toUpperCase() + name.slice(1)
    }
    return seg.charAt(0).toUpperCase() + seg.slice(1)
  })

  return prefix + parts.join('')
}

function extractPathParams(path: string): string[] {
  const matches = path.match(/\{([^}]+)\}/g)
  if (matches === null) return []
  return matches.map((m) => m.slice(1, -1))
}

function operationHasBody(operation: OperationObject): boolean {
  return operation.requestBody !== undefined
}

function operationHasQueryParams(operation: OperationObject): boolean {
  const params = operation.parameters as (OpenAPIV3_1.ParameterObject | ReferenceObject)[] | undefined
  if (params === undefined) return false
  return params.some((p) => {
    if ('$ref' in p) return false
    return (p as OpenAPIV3_1.ParameterObject).in === 'query'
  })
}

function operationHasRequiredQueryParams(operation: OperationObject): boolean {
  const params = operation.parameters as (OpenAPIV3_1.ParameterObject | ReferenceObject)[] | undefined
  if (params === undefined) return false
  return params.some((p) => {
    if ('$ref' in p) return false
    const param = p as OpenAPIV3_1.ParameterObject
    return param.in === 'query' && param.required === true
  })
}

interface OperationInfo {
  funcName: string
  pathParams: string[]
  hasBody: boolean
  hasQueryParams: boolean
  hasRequiredQueryParams: boolean
}

/**
 * Generate server.ts — a createServerClient() factory that pre-binds a
 * per-request ClientConfig to every generated client function.
 *
 * Useful for Next.js App Router / RSC where auth config differs per request
 * and passing it to every function call would be repetitive.
 *
 * Generated pattern (explicit signatures per operation):
 *   export function createServerClient(config: Partial<ClientConfig>) {
 *     return {
 *       listTasks: (params?: Parameters<typeof listTasks>[0]) => listTasks(params, config),
 *       getTask: (id: Parameters<typeof getTask>[0]) => getTask(id, config),
 *       createTask: (body: Parameters<typeof createTask>[0]) => createTask(body, config),
 *       updateTask: (id: Parameters<typeof updateTask>[0], body: Parameters<typeof updateTask>[1]) => updateTask(id, body, config),
 *       deleteTask: (id: Parameters<typeof deleteTask>[0]) => deleteTask(id, config),
 *     }
 *   }
 */
export function generateServer(spec: OpenAPIV3_1.Document): GeneratedFile {
  const paths = spec.paths as Record<string, Record<string, OperationObject>> | undefined
  const operations: OperationInfo[] = []

  if (paths !== undefined) {
    for (const [path, pathItem] of Object.entries(paths)) {
      for (const method of SUPPORTED_METHODS) {
        const operation = pathItem[method] as OperationObject | undefined
        if (operation === undefined) continue

        let funcName: string
        if (operation.operationId !== undefined) {
          const id = operation.operationId
          funcName = id.charAt(0).toLowerCase() + id.slice(1)
        } else {
          funcName = deriveOperationName(method, path)
        }

        operations.push({
          funcName,
          pathParams: extractPathParams(path),
          hasBody: operationHasBody(operation),
          hasQueryParams: operationHasQueryParams(operation),
          hasRequiredQueryParams: operationHasRequiredQueryParams(operation),
        })
      }
    }
  }

  const funcNames = operations.map((op) => op.funcName)

  const lines: string[] = []
  lines.push('// This file is auto-generated by @codewithagents/openapi-gen — do not edit')
  lines.push('')
  lines.push("import type { ClientConfig } from './client-config.js'")
  if (funcNames.length > 0) {
    lines.push(`import { ${funcNames.join(', ')} } from './client.js'`)
  }
  lines.push('')
  lines.push('/**')
  lines.push(' * Create a server-side API client with config pre-applied to every function.')
  lines.push(' * Ideal for Next.js App Router / RSC where auth headers are per-request.')
  lines.push(' *')
  lines.push(' * @example')
  lines.push(' * const api = createServerClient(await getServerConfig())')
  lines.push(' * const tasks = await api.listTasks()')
  lines.push(' */')
  lines.push('export function createServerClient(config: Partial<ClientConfig>) {')
  lines.push('  return {')

  for (const op of operations) {
    const { funcName, pathParams, hasBody, hasQueryParams, hasRequiredQueryParams } = op

    // Build explicit parameter list (left to right: path params, body?, query params?)
    // Last param in the client function is always config — never include it here.
    const sigParts: string[] = []
    const callArgs: string[] = []
    let argIndex = 0

    // Path params
    for (const param of pathParams) {
      sigParts.push(`${param}: Parameters<typeof ${funcName}>[${argIndex}]`)
      callArgs.push(param)
      argIndex++
    }

    // Body (if present)
    if (hasBody) {
      sigParts.push(`body: Parameters<typeof ${funcName}>[${argIndex}]`)
      callArgs.push('body')
      argIndex++
    }

    // Query params (if present)
    if (hasQueryParams) {
      const paramsToken = hasRequiredQueryParams
        ? `params: Parameters<typeof ${funcName}>[${argIndex}]`
        : `params?: Parameters<typeof ${funcName}>[${argIndex}]`
      sigParts.push(paramsToken)
      callArgs.push('params')
    }

    const sig = sigParts.join(', ')
    const call = [...callArgs, 'config'].join(', ')
    lines.push(`    ${funcName}: (${sig}) => ${funcName}(${call}),`)
  }

  lines.push('  }')
  lines.push('}')
  lines.push('')

  return {
    filename: 'server.ts',
    content: lines.join('\n'),
  }
}
